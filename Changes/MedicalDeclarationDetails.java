package src.com.intervest.hercules;

public class MedicalDeclarationDetails {

	private String agentRefPolicyNumber;
	private String periodOfInsurance;
	private String expiryDate;
	private String dateOfDeparture;
	private String dateOfReturn;
	private String geographicalLimit;

	public String getAgentRefPolicyNumber() {
		return agentRefPolicyNumber;
	}

	public void setAgentRefPolicyNumber(String agentRefPolicyNumber) {
		this.agentRefPolicyNumber = agentRefPolicyNumber;
	}

	public String getPeriodOfInsurance() {
		return periodOfInsurance;
	}

	public void setPeriodOfInsurance(String periodOfInsurance) {
		this.periodOfInsurance = periodOfInsurance;
	}

	public String getExpiryDate() {
		return expiryDate;
	}

	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}

	public String getDateOfDeparture() {
		return dateOfDeparture;
	}

	public void setDateOfDeparture(String dateOfDeparture) {
		this.dateOfDeparture = dateOfDeparture;
	}

	public String getDateOfReturn() {
		return dateOfReturn;
	}

	public void setDateOfReturn(String dateOfReturn) {
		this.dateOfReturn = dateOfReturn;
	}

	public String getGeographicalLimit() {
		return geographicalLimit;
	}

	public void setGeographicalLimit(String geographicalLimit) {
		this.geographicalLimit = geographicalLimit;
	}

}
