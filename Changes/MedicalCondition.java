package src.com.intervest.hercules;

public class MedicalCondition {

	private String personName;
	private String personAge;
	private String screeningQnA;
	private String freeMedicalCondition;

	public String getPersonName() {
		return personName;
	}

	public void setPersonName(String personName) {
		this.personName = personName;
	}

	public String getPersonAge() {
		return personAge;
	}

	public void setPersonAge(String personAge) {
		this.personAge = personAge;
	}

	public String getScreeningQnA() {
		return screeningQnA;
	}

	public void setScreeningQnA(String screeningQnA) {
		this.screeningQnA = screeningQnA;
	}

	public String getFreeMedicalCondition() {
		return freeMedicalCondition;
	}

	public void setFreeMedicalCondition(String freeMedicalCondition) {
		this.freeMedicalCondition = freeMedicalCondition;
	}

}
